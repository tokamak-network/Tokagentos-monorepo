/**
 * Mirror branded MILADY_* env vars into TOKAGENT_* so `tokagent/packages` only reads TOKAGENT_*.
 * Call once after loading `.env.worktree` / dotenv and before resolving ports or spawning children.
 */
export function syncTokagentEnvAliases() {
  const pairs = [
    ["MILADY_NAMESPACE", "TOKAGENT_NAMESPACE"],
    ["MILADY_STATE_DIR", "TOKAGENT_STATE_DIR"],
    ["MILADY_CONFIG_PATH", "TOKAGENT_CONFIG_PATH"],
    ["MILADY_OAUTH_DIR", "TOKAGENT_OAUTH_DIR"],
    ["MILADY_AGENT_ORCHESTRATOR", "TOKAGENT_AGENT_ORCHESTRATOR"],
    ["MILADY_CLOUD_PROVISIONED", "TOKAGENT_CLOUD_PROVISIONED"],
    ["MILADY_CHAT_GENERATION_TIMEOUT_MS", "TOKAGENT_CHAT_GENERATION_TIMEOUT_MS"],
    ["MILADY_SKIP_LOCAL_PLUGIN_ROLES", "TOKAGENT_SKIP_LOCAL_PLUGIN_ROLES"],
    ["MILADY_SETTINGS_DEBUG", "TOKAGENT_SETTINGS_DEBUG"],
    ["VITE_MILADY_SETTINGS_DEBUG", "VITE_TOKAGENT_SETTINGS_DEBUG"],
    [
      "MILADY_GOOGLE_OAUTH_DESKTOP_CLIENT_ID",
      "TOKAGENT_GOOGLE_OAUTH_DESKTOP_CLIENT_ID",
    ],
    ["MILADY_API_PORT", "TOKAGENT_API_PORT"],
    ["MILADY_API_BIND", "TOKAGENT_API_BIND"],
    ["MILADY_API_TOKEN", "TOKAGENT_API_TOKEN"],
    ["MILADY_ALLOWED_ORIGINS", "TOKAGENT_ALLOWED_ORIGINS"],
    ["MILADY_ALLOWED_HOSTS", "TOKAGENT_ALLOWED_HOSTS"],
    ["MILADY_ALLOW_NULL_ORIGIN", "TOKAGENT_ALLOW_NULL_ORIGIN"],
    ["MILADY_DISABLE_AUTO_API_TOKEN", "TOKAGENT_DISABLE_AUTO_API_TOKEN"],
    [
      "MILADY_TASK_AGENT_AUTH_TRUSTED_HOSTS",
      "TOKAGENT_TASK_AGENT_AUTH_TRUSTED_HOSTS",
    ],
    [
      "MILADY_TASK_AGENT_AUTH_API_BASE_URL",
      "TOKAGENT_TASK_AGENT_AUTH_API_BASE_URL",
    ],
    ["MILADY_PORT", "TOKAGENT_UI_PORT"],
  ];
  for (const [from, to] of pairs) {
    if (!process.env[to] && process.env[from]) {
      process.env[to] = process.env[from];
    }
  }
  if (!process.env.TOKAGENT_CLOUD_MANAGED_AGENTS_API_SEGMENT) {
    process.env.TOKAGENT_CLOUD_MANAGED_AGENTS_API_SEGMENT = "milady";
  }
}
