// Output filters for API server logs.
// ---------------------------------------------------------------------------

const SUPPRESS_RE = /^\s*(Info|Warn|Debug|Trace)\s/;
const SUPPRESS_UNSTRUCTURED_RE = /^\[dotenv[@\d]/;
const STARTUP_RE =
  /\[eliza(?:-api)?\]|\[(eliza|eliza)(?:-api)?\]|runtime bootstrap|\[(eliza|eliza)(?:-api)?\]|runtime ready|\[(eliza|eliza)(?:-api)?\]|runtime created|api server ready|plugin.*load|startup.*complete|\d+ms|\[PTYService|\[SwarmCoordinator\]|Triage:/i;

function createErrorFilter(dest) {
  let buf = "";
  return (chunk) => {
    buf += chunk.toString();
    const lines = buf.split("\n");
    buf = lines.pop();
    for (const line of lines) {
      if (
        line.trim() &&
        !SUPPRESS_RE.test(line) &&
        !SUPPRESS_UNSTRUCTURED_RE.test(line)
      ) {
        dest.write(`${line}\n`);
      }
    }
  };
}

function createStartupFilter(dest) {
  let buf = "";
  let lastLine = "";
  let repeatCount = 0;
  return (chunk) => {
    buf += chunk.toString();
    const lines = buf.split("\n");
    buf = lines.pop();
    for (const line of lines) {
      const trimmed = line.trim();
      if (!trimmed) continue;
      if (SUPPRESS_UNSTRUCTURED_RE.test(trimmed)) continue;
      if (/embedding dimensions mismatch/i.test(trimmed)) continue;

      if (trimmed === lastLine) {
        repeatCount += 1;
        if (repeatCount > 2) continue;
      } else {
        lastLine = trimmed;
        repeatCount = 0;
      }

      const isWarnOrError = /^\s*(Warn|Error)\s/.test(trimmed);
      const isStartupLine = STARTUP_RE.test(trimmed);
      if (isWarnOrError || isStartupLine) {
        dest.write(`${line}\n`);
      }
    }
  };
}

// ---------------------------------------------------------------------------
// Port cleanup — force-kill zombie processes on our dev ports
// ---------------------------------------------------------------------------

function killPort(port) {
  try {
    if (process.platform === "win32") {
      const out = execSync(
        `netstat -ano | findstr :${port} | findstr LISTENING`,
        { encoding: "utf-8", stdio: ["pipe", "pipe", "ignore"] },
      );
      const pids = new Set(
        out
          .split("\n")
          .map((l) => l.trim().split(/\s+/).pop())
          .filter(Boolean),
      );
      for (const pid of pids) {
        try {
          execSync(`taskkill /F /PID ${pid}`, { stdio: "ignore" });
        } catch {
          /* already dead */
        }
      }
    } else {
      execSync(`lsof -ti :${port} | xargs kill -9 2>/dev/null`, {
        stdio: "ignore",
      });
    }
  } catch {
    // No process found — port is clean
  }
}

// ---------------------------------------------------------------------------
// Wait for a TCP port to accept connections
// ---------------------------------------------------------------------------

function waitForPort(port, { timeout = 120_000, interval = 500 } = {}) {
  return new Promise((resolve, reject) => {
    const deadline = Date.now() + timeout;
    let activeSocket = null;

    function attempt() {
      if (Date.now() > deadline) {
        if (activeSocket) {
          activeSocket.destroy();
          activeSocket = null;
        }
        reject(
          new Error(
            `Timed out waiting for port ${port} after ${timeout / 1000}s`,
          ),
        );
        return;
      }
      activeSocket = createConnection({ port, host: "127.0.0.1" });
      activeSocket.once("connect", () => {
        activeSocket.destroy();
        activeSocket = null;
        resolve();
      });
      activeSocket.once("error", () => {
        activeSocket.destroy();
        activeSocket = null;
        setTimeout(attempt, interval);
      });
    }

    attempt();
  });
}

// ---------------------------------------------------------------------------
// Wait for the agent runtime to be ready (not just the TCP port).
// Polls GET /api/health and resolves when { ready: true }.
// Handles bun --watch restarts gracefully (connection resets → retry).
// ---------------------------------------------------------------------------

async function waitForAgentReady(
  port,
  { timeout = 120_000, interval = 1000 } = {},
) {
  const deadline = Date.now() + timeout;
  const url = `http://127.0.0.1:${port}/api/health`;

  while (Date.now() < deadline) {
    try {
      const res = await fetch(url, { signal: AbortSignal.timeout(3000) });
      if (res.ok) {
        const data = await res.json();
        if (data.ready) return;
      }
    } catch {
      // API not up yet or restarting — keep polling.
    }
    await new Promise((r) => setTimeout(r, interval));
  }

  throw new Error(
    `Agent runtime not ready after ${timeout / 1000}s (port ${port} is up but /api/health never reported ready)`,
  );
}

// ---------------------------------------------------------------------------
// Orphan cleanup (startup only) — never kills arbitrary Bun; PID/name-wide pkill is avoided.
// Only processes whose command line ties them to this repo or Eliza workspace dirs.
// ---------------------------------------------------------------------------

function killOrphanedWorkspaceProcesses() {
  if (process.platform === "win32") return; // spawn-helper is Unix only

  let repoRoot;
  try {
    repoRoot = realpathSync(cwd);
  } catch {
    repoRoot = cwd;
  }

  let psOut;
  try {
    psOut = execSync("ps axo pid=,command=", {
      encoding: "utf8",
      maxBuffer: 10 * 1024 * 1024,
      stdio: ["pipe", "pipe", "ignore"],
    });
  } catch {
    return;
  }

  const workspaceDirRe =
    /\.eliza\/workspaces|\.elizaai\/workspaces|\.eliza\/workspaces|\.elizaai\/workspaces/i;
  const ptyWorkerRe = /pty-worker\.js/i;

  const workspacePids = [];
  const ptyPids = [];

  for (const line of psOut.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    const sp = trimmed.indexOf(" ");
    if (sp < 1) continue;
    const pidStr = trimmed.slice(0, sp).trim();
    const cmd = trimmed.slice(sp + 1);
    const pid = Number.parseInt(pidStr, 10);
    if (!Number.isFinite(pid) || pid <= 0) continue;

    if (workspaceDirRe.test(cmd)) {
      workspacePids.push(pid);
      continue;
    }
    if (ptyWorkerRe.test(cmd) && cmd.includes(repoRoot)) {
      ptyPids.push(pid);
    }
  }

  const killPids = (label, pids) => {
    if (!pids.length) return;
    console.log(`[dev-ui] Killing ${pids.length} orphaned ${label}…`);
    try {
      execSync(`kill -9 ${pids.join(" ")} 2>/dev/null`, { stdio: "ignore" });
    } catch {
      /* ignore */
    }
  };

  killPids("workspace process(es)", workspacePids);
  killPids("repo-scoped pty-worker(s)", ptyPids);
}

