#!/usr/bin/env node
/**
 * i18n Keyword Code Generator
 *
 * Reads keyword JSON files from src/i18n/keywords/*.keywords.json and generates
 * language-native keyword modules for TypeScript, Rust, and Python.
 *
 * Usage:
 *   node scripts/generate-keywords.mjs [--target ts|rust|python|all]
 *
 * JSON format:
 *   {
 *     "locales": ["zh-CN", "ko", "es", ...],
 *     "entries": {
 *       "contextSignal.gmail.strong": {
 *         "base": ["email", "gmail", "inbox", ...],
 *         "zh-CN": ["邮件", "电子邮件", ...],
 *         ...
 *       }
 *     }
 *   }
 *
 * Output: src/i18n/generated/validation-keyword-data.ts
 *
 * The generated TS file exports a VALIDATION_KEYWORD_DOCS object that is
 * API-compatible with the hand-written version it replaces. Consumers
 * import matching utilities from keyword-matching.ts (hand-written).
 */

import { readFileSync, writeFileSync, mkdirSync, readdirSync } from "node:fs";
import { dirname, join, basename } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const keywordsDir = join(__dirname, "..", "src", "i18n", "keywords");
const generatedDir = join(__dirname, "..", "src", "i18n", "generated");

const args = process.argv.slice(2);
const targetIdx = args.indexOf("--target");
const target = targetIdx !== -1 ? args[targetIdx + 1] : "ts";

// --- Load all keyword JSON files ---

function loadKeywordFiles() {
  const files = readdirSync(keywordsDir).filter((f) =>
    f.endsWith(".keywords.json"),
  );
  if (files.length === 0) {
    console.error("No *.keywords.json files found in", keywordsDir);
    process.exit(1);
  }

  const allEntries = {};
  const allLocales = new Set();

  for (const file of files.sort()) {
    const data = JSON.parse(readFileSync(join(keywordsDir, file), "utf-8"));
    for (const locale of data.locales || []) {
      allLocales.add(locale);
    }
    for (const [key, value] of Object.entries(data.entries || {})) {
      if (key in allEntries) {
        console.warn(`  Warning: duplicate key "${key}" in ${file}, overwriting`);
      }
      allEntries[key] = value;
    }
    console.log(`  Loaded ${file}: ${Object.keys(data.entries || {}).length} entries`);
  }

  return { entries: allEntries, locales: [...allLocales].sort() };
}

// --- TypeScript Generator ---

function generateTypeScript(entries, locales) {
  const lines = [];

  lines.push("// @generated by scripts/generate-keywords.mjs — DO NOT EDIT");
  lines.push("// Source: src/i18n/keywords/*.keywords.json");
  lines.push("//");
  lines.push(
    "// This file contains the keyword data. Matching utilities are in keyword-matching.ts.",
  );
  lines.push("");
  lines.push(
    'import type { CharacterLanguage } from "../../contracts/onboarding.js";',
  );
  lines.push("");
  lines.push("type ValidationKeywordDoc = {");
  lines.push("  base?: string;");
  lines.push("  locales?: Partial<Record<CharacterLanguage, string>>;");
  lines.push("};");
  lines.push("");
  lines.push("type ValidationKeywordTree = {");
  lines.push("  [key: string]: ValidationKeywordTree | ValidationKeywordDoc;");
  lines.push("};");
  lines.push("");

  // Build the nested object structure from dotted keys
  const tree = {};
  for (const [dottedKey, entry] of Object.entries(entries)) {
    const parts = dottedKey.split(".");
    let current = tree;
    for (let i = 0; i < parts.length - 1; i++) {
      if (!(parts[i] in current)) {
        current[parts[i]] = {};
      }
      current = current[parts[i]];
    }
    current[parts[parts.length - 1]] = entry;
  }

  lines.push("export const VALIDATION_KEYWORD_DOCS = {");
  emitTreeTS(tree, lines, 1);
  lines.push("} as const satisfies ValidationKeywordTree;");
  lines.push("");

  return lines.join("\n") + "\n";
}

function indent(level) {
  return "  ".repeat(level);
}

function termsToMultiline(terms) {
  // Convert array of terms back to the newline-separated string format
  // that the existing consumers expect (splitKeywordDoc splits on \n)
  return terms.join("\n");
}

function emitTreeTS(node, lines, depth) {
  const keys = Object.keys(node);
  for (let i = 0; i < keys.length; i++) {
    const key = keys[i];
    const value = node[key];
    const needsQuote = key.includes("-") || key.includes(".");
    const quotedKey = needsQuote ? `"${key}"` : key;

    if (isLeafEntry(value)) {
      // This is a keyword doc (has "base" or locale keys)
      lines.push(`${indent(depth)}${quotedKey}: {`);
      if (value.base) {
        lines.push(
          `${indent(depth + 1)}base: ${JSON.stringify(termsToMultiline(value.base))},`,
        );
      }
      const localeKeys = Object.keys(value).filter((k) => k !== "base");
      if (localeKeys.length > 0) {
        lines.push(`${indent(depth + 1)}locales: {`);
        for (const locale of localeKeys) {
          const localeKey =
            locale.includes("-") ? `"${locale}"` : locale;
          lines.push(
            `${indent(depth + 2)}${localeKey}: ${JSON.stringify(termsToMultiline(value[locale]))},`,
          );
        }
        lines.push(`${indent(depth + 1)}},`);
      }
      lines.push(`${indent(depth)}},`);
    } else {
      // Subtree
      lines.push(`${indent(depth)}${quotedKey}: {`);
      emitTreeTS(value, lines, depth + 1);
      lines.push(`${indent(depth)}},`);
    }
  }
}

function isLeafEntry(value) {
  if (!value || typeof value !== "object") return false;
  // A leaf entry has "base" (an array) or locale keys (arrays)
  // A subtree has values that are objects with further nesting
  if (Array.isArray(value.base)) return true;
  // Check if any value is an array (locale data)
  for (const v of Object.values(value)) {
    if (Array.isArray(v)) return true;
  }
  return false;
}

// --- TypeScript Standalone Generator (for @elizaos/core, no shared dep) ---

function generateTypeScriptStandalone(entries, locales) {
  const lines = [];

  lines.push("// @generated by @elizaos/shared/scripts/generate-keywords.mjs — DO NOT EDIT");
  lines.push("// Source: @elizaos/shared/src/i18n/keywords/*.keywords.json");
  lines.push("");
  lines.push(`export const VALIDATION_KEYWORD_LOCALES = ${JSON.stringify(locales)} as const;`);
  lines.push("");
  lines.push("export type ValidationKeywordLocale = (typeof VALIDATION_KEYWORD_LOCALES)[number];");
  lines.push("");
  lines.push("type ValidationKeywordDoc = {");
  lines.push("  base?: string;");
  lines.push("  locales?: Partial<Record<ValidationKeywordLocale, string>>;");
  lines.push("};");
  lines.push("");
  lines.push("type ValidationKeywordTree = {");
  lines.push("  [key: string]: ValidationKeywordTree | ValidationKeywordDoc;");
  lines.push("};");
  lines.push("");

  // Build the nested object structure (same as shared, different type import)
  const tree = {};
  for (const [dottedKey, entry] of Object.entries(entries)) {
    const parts = dottedKey.split(".");
    let current = tree;
    for (let i = 0; i < parts.length - 1; i++) {
      if (!(parts[i] in current)) {
        current[parts[i]] = {};
      }
      current = current[parts[i]];
    }
    current[parts[parts.length - 1]] = entry;
  }

  lines.push("export const VALIDATION_KEYWORD_DOCS = {");
  emitTreeTS(tree, lines, 1);
  lines.push("} as const satisfies ValidationKeywordTree;");
  lines.push("");

  return lines.join("\n") + "\n";
}

// --- Rust Generator ---

function generateRust(entries, locales) {
  const lines = [];
  lines.push("// @generated by scripts/generate-keywords.mjs — DO NOT EDIT");
  lines.push("// Source: src/i18n/keywords/*.keywords.json");
  lines.push("");
  lines.push("");

  for (const [key, entry] of Object.entries(entries)) {
    const constName = key.replace(/\./g, "_").toUpperCase();
    if (entry.base) {
      lines.push(`pub const ${constName}_BASE: &[&str] = &[`);
      for (const term of entry.base) {
        lines.push(`    ${JSON.stringify(term)},`);
      }
      lines.push("];");
      lines.push("");
    }
    for (const locale of locales) {
      if (entry[locale]) {
        const localeSuffix = locale.replace("-", "_").toUpperCase();
        lines.push(
          `pub const ${constName}_${localeSuffix}: &[&str] = &[`,
        );
        for (const term of entry[locale]) {
          lines.push(`    ${JSON.stringify(term)},`);
        }
        lines.push("];");
        lines.push("");
      }
    }
  }

  // Lookup function
  lines.push(
    "pub fn get_keyword_terms(key: &str, locale: Option<&str>) -> Vec<&'static str> {",
  );
  lines.push("    let mut terms = Vec::new();");
  lines.push("    match key {");
  for (const [key, entry] of Object.entries(entries)) {
    const constName = key.replace(/\./g, "_").toUpperCase();
    if (entry.base) {
      lines.push(`        ${JSON.stringify(key)} => {`);
      lines.push(`            terms.extend_from_slice(${constName}_BASE);`);
      lines.push("            match locale {");
      for (const locale of locales) {
        if (entry[locale]) {
          const localeSuffix = locale.replace("-", "_").toUpperCase();
          lines.push(
            `                Some(${JSON.stringify(locale)}) => terms.extend_from_slice(${constName}_${localeSuffix}),`,
          );
        }
      }
      lines.push("                _ => {}");
      lines.push("            }");
      lines.push("        }");
    }
  }
  lines.push('        _ => {}');
  lines.push("    }");
  lines.push("    terms");
  lines.push("}");
  lines.push("");

  return lines.join("\n") + "\n";
}

// --- Python Generator ---

function generatePython(entries, locales) {
  const lines = [];
  lines.push("# @generated by scripts/generate-keywords.mjs — DO NOT EDIT");
  lines.push("# Source: src/i18n/keywords/*.keywords.json");
  lines.push("");
  lines.push("from typing import Optional");
  lines.push("");
  lines.push("KEYWORD_DATA: dict[str, dict[str, list[str]]] = {");

  for (const [key, entry] of Object.entries(entries)) {
    lines.push(`    ${JSON.stringify(key)}: {`);
    if (entry.base) {
      lines.push(`        "base": ${JSON.stringify(entry.base)},`);
    }
    for (const locale of locales) {
      if (entry[locale]) {
        lines.push(`        ${JSON.stringify(locale)}: ${JSON.stringify(entry[locale])},`);
      }
    }
    lines.push("    },");
  }

  lines.push("}");
  lines.push("");
  lines.push("");
  lines.push(
    "def get_keyword_terms(key: str, locale: Optional[str] = None, include_all_locales: bool = False) -> list[str]:",
  );
  lines.push('    """Get keyword terms for a given key, optionally filtered by locale."""');
  lines.push("    entry = KEYWORD_DATA.get(key)");
  lines.push("    if entry is None:");
  lines.push("        return []");
  lines.push("    terms: list[str] = []");
  lines.push('    terms.extend(entry.get("base", []))');
  lines.push("    if include_all_locales:");
  lines.push("        for k, v in entry.items():");
  lines.push('            if k != "base":');
  lines.push("                terms.extend(v)");
  lines.push("    elif locale:");
  lines.push("        terms.extend(entry.get(locale, []))");
  lines.push("    return terms");
  lines.push("");

  return lines.join("\n") + "\n";
}

// --- Main ---

console.log("Generating keyword code from JSON...\n");
const { entries, locales } = loadKeywordFiles();
console.log(
  `\nTotal: ${Object.keys(entries).length} entries, ${locales.length} locales\n`,
);

mkdirSync(generatedDir, { recursive: true });

if (target === "ts" || target === "all") {
  const ts = generateTypeScript(entries, locales);
  const outPath = join(generatedDir, "validation-keyword-data.ts");
  writeFileSync(outPath, ts, "utf-8");
  console.log(`  TypeScript (shared): ${outPath}`);

  // Also generate for the typescript (@elizaos/core) package — same data,
  // standalone file with no import dependency on @elizaos/shared.
  const tsCorePath = join(
    __dirname,
    "..",
    "..",
    "typescript",
    "src",
    "i18n",
    "generated",
  );
  mkdirSync(tsCorePath, { recursive: true });
  const tsCoreContent = generateTypeScriptStandalone(entries, locales);
  const tsCoreOutPath = join(tsCorePath, "validation-keyword-data.ts");
  writeFileSync(tsCoreOutPath, tsCoreContent, "utf-8");
  console.log(`  TypeScript (core):   ${tsCoreOutPath}`);
}

if (target === "rust" || target === "all") {
  const rs = generateRust(entries, locales);
  const outPath = join(generatedDir, "keywords.rs");
  writeFileSync(outPath, rs, "utf-8");
  console.log(`  Rust: ${outPath}`);
}

if (target === "python" || target === "all") {
  const py = generatePython(entries, locales);
  const outPath = join(generatedDir, "keywords.py");
  writeFileSync(outPath, py, "utf-8");
  console.log(`  Python: ${outPath}`);
}

console.log("\nDone!");
